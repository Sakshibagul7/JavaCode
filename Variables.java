public class Variables {
    public static void main(String args []) {
    int a =10;
    int b=5;
    System.out.println(a);
    System.out.println(b);


    // changing value of a
    a=50;
    System.out.println(a);
    a=b;
    System.out.println(a);

    // Sum of two variables

    int w=10;
    int v=20;
    int sum = w+v;
    System.out.println("sum="+ sum);

    

}
    
}